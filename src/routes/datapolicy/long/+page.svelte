<MiddleCard><h1 class="h1">Datenschutzgrundverordnung</h1>
    <p>
        Ihnen stehen gemäß DSGVO sog. Betroffenenrechte zu, d. h. Rechte, die Sie als im Einzelfall betroffene Person
        ausüben können. Diese Rechte können Sie gegenüber der Universität Würzburg geltend machen. Sie ergeben sich aus
        der DSGVO:
        Recht auf Widerruf der Einwilligung Art. 7 Abs. 3 DSGVO <br>
        Sie haben das Recht, Ihre Einwilligung jederzeit gegenüber der verantwortlichen Stelle zu widerrufen. Dies hat
        zur Folge, dass die Datenverarbeitung, die auf dieser Einwilligung beruht, für die Zukunft nicht mehr
        fortgeführt werden darf.
        <br>
        Recht auf Auskunft, Art. 15 DSGVO <br>
        Sie haben das Recht auf Auskunft über die Sie betreffenden gespeicherten personenbezogenen Daten. <br>
        Recht auf Berichtigung, Art. 16 DSGVO <br>
        Wenn Sie feststellen, dass falsche Daten zu Ihrer Person verarbeitet werden, können Sie Berichtigung verlangen.
        Unvollständige Daten müssen unter Berücksichtigung des Zwecks der Verarbeitung vervollständigt werden.
        <br>
        Recht auf Löschung, Art. 17 DSGVO <br>
        Sie haben das Recht, die Löschung Ihrer Daten zu verlangen, wenn bestimmte Löschgründe vorliegen. Dies ist
        insbesondere der Fall, wenn diese zu dem Zweck, zu dem sie ursprünglich erhoben oder verarbeitet wurden, nicht
        mehr erforderlich sind oder Sie Ihre Einwilligung widerrufen.
        <br>
        Recht auf Einschränkung der Verarbeitung, Art. 18 DSGVO <br>
        Sie haben das Recht auf Einschränkung der Verarbeitung Ihrer Daten. Dies bedeutet, dass Ihre Daten zwar nicht
        gelöscht, aber gekennzeichnet werden, um Ihre weitere Verarbeitung oder Nutzung einzuschränken.
        <br>
        Recht auf Datenübertragbarkeit, Art. 20 DSGVO <br>
        Sie haben das Recht, die Daten, die Sie uns zur Verfügung gestellt haben, in einem gängigen elektronischen
        Format von uns zu verlangen.
        <br>
        Recht auf Widerspruch gegen unzumutbare Datenverarbeitung, Art. 21 DSGVO <br>
        Sie haben grundsätzlich ein allgemeines Widerspruchsrecht auch gegen rechtmäßige Datenverarbeitungen, die im
        öffentlichen Interesse liegen, in Ausübung öffentlicher Gewalt oder aufgrund des berechtigten Interesses einer
        Stelle erfolgen.
        <br>
        Recht auf Beschwerde bei einer Aufsichtsbehörde, Art. 77 DSGVO <br>
        Sie haben das Recht sich unbeschadet eines anderweitigen verwaltungsrechtlichen oder gerichtlichen Rechtsbehelfs
        bei einer Aufsichtsbehörde zu beschweren.
        <br>
